from abc import ABC, abstractmethod

class Shape(ABC):
    @abstractmethod
    def area(self):
        pass

class Rectangle(Shape):
    def __init__(self, width, height):
        self.width = width
        self.height = height
    
    def area(self):
        return self.width * self.height

# Testing
rect = Rectangle(10, 5)
print(f"Rectangle Area: {rect.area()}") # Rectangle Area: 50